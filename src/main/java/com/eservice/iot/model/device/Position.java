package com.eservice.iot.model.device;

public class Position {

    /**
     * x : 0.0
     * y : 0.0
     */
    private double x;
    private double y;

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
